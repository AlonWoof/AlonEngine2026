class ObjectTestCube : public Object
{
public:

	Shader* mShader;
    unsigned int VBO, VAO;
    float angle = 0.0f;

    unsigned int textureID;

	ObjectTestCube(glm::vec3 nPos = glm::vec3(0.0f,0.0f,0.0f))
	{
        
        Init(nPos, glm::vec3(0,0,0));

		mShader = new Shader("default");
		mShader->use();

        textureID = loadTexture("./data/textures/NakieHomu.png");

        glGenVertexArrays(1, &VAO);
        glGenBuffers(1, &VBO);

        glBindVertexArray(VAO);
        glBindBuffer(GL_ARRAY_BUFFER, VBO);
        glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

        // position attribute
        glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);
        glEnableVertexAttribArray(0);
        // texture coord attribute
        glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float)));
        glEnableVertexAttribArray(1);
	};

	void Update()
	{
		//if(!mShader)
			//mShader = new Shader("default.vs", "default.fs");

        angle += 16.0f * gDeltaTime;
        //Position.x = (float)sin(glfwGetTime());
       // Position.y = (float)sin(glfwGetTime());
        //Position.z = -5.0f;
	}

	void Draw()
	{
        mShader->use();
        mShader->setMat4("projection", gProjectionMatrix);
        mShader->setMat4("view", gViewMatrix);

        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D, textureID);

		glm::mat4 model = glm::mat4(1.0f);
        model = glm::translate(model, Position);
        model = glm::rotate(model, glm::radians(angle), glm::vec3(1.0f, 1.0f, 1.0f));
        mShader->setMat4("model", model);

        glBindVertexArray(VAO);
        glDrawArrays(GL_TRIANGLES, 0, 36);
	}
};



