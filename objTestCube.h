class ObjectTestCube : public Object
{
public:

    Material* material;
    unsigned int VBO, VAO;
    float angle = 0.0f;

    unsigned int textureID;

	ObjectTestCube(glm::vec3 nPos = glm::vec3(0.0f,0.0f,0.0f))
	{
        
        Init(nPos, glm::vec3(0,0,0));

        material = new Material("./data/materials/test.mat");

        glGenVertexArrays(1, &VAO);
        glGenBuffers(1, &VBO);

        
        glBindBuffer(GL_ARRAY_BUFFER, VBO);
        glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);
        glBindVertexArray(VAO);

        // position attribute
        glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
        glEnableVertexAttribArray(0);
        // normal attribute
        glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));
        glEnableVertexAttribArray(1);
        // texcoord attribute
        glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));
        glEnableVertexAttribArray(2);


	};

	void Update()
	{
		//if(!mShader)
			//mShader = new Shader("default.vs", "default.fs");

        angle += 16.0f * gDeltaTime;
        //Position.x = (float)sin(glfwGetTime());
       // Position.y = (float)sin(glfwGetTime());
        //Position.z = -5.0f;
	}

	void Draw()
	{

		glm::mat4 model = glm::mat4(1.0f);
        model = glm::translate(model, Position);
        model = glm::rotate(model, glm::radians(angle), glm::vec3(1.0f, 1.0f, 1.0f));
        material->Set(model);

        glBindVertexArray(VAO);
        glDrawArrays(GL_TRIANGLES, 0, 36);
	}
};



